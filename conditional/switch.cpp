#include<iostream>
using namespace std;
int main(){
    int day;
    cin>>day;
    switch (day){
        case 1:
        cout<<"Monday"<<endl;
        break;
        case 2:
        cout<<"Tuesday"<<endl;
        break;
case 3:
cout<<"wednesday"<<endl;
break;

case 4:
cout<<"thursday"<<endl;
break;

case 5:
cout<<"friday"<<endl ;
break;
default:
cout<<"sunday"<<endl;
   }
}